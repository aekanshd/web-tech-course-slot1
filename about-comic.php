          <h4>Brief Intro To The Novel</h4>
          <p>Haruhi Suzumiya (涼宮ハルヒ Suzumiya Haruhi) is a series of light novels written by Nagaru Tanigawa and illustrated by Noizi Ito and which were adapted into other media. Initially released in Japan in 2003 with the novel The Melancholy of Haruhi Suzumiya, it was followed by ten additional volumes, an anime television series adaptation, four manga series, an animated film, two original net animation series and several video games.</p>
          <p>After the anime adaptation airing in 2006, publishing company Kadokawa Shoten received offers for licensing the novels and their adaptations. The novels are licensed for English language release in the United States by Little, Brown and Company, for young readers by Yen Press and the anime adaptation was licensed for North American distribution by Kadokawa Pictures USA division which then sub-licensed production and distribution to Bandai Entertainment. The anime is currently licensed by Funimation.</p>
          <h4>Plot</h4>
          <p>Kyon is a student at North High School in Nishinomiya. He is dragged along by his classmate, the titular character Haruhi Suzumiya, an eccentric schoolgirl who seeks supernatural phenomena and figures such as aliens, time travelers, and espers. With Kyon's reluctant help, Haruhi establishes a club called the "SOS Brigade" (SOS団 Esu-Ō-Esu Dan), short for "Spreading excitement all Over the world with Haruhi Suzumiya Brigade" (世界を大いに盛り上げるための涼宮ハルヒの団 Sekai o Ōini Moriageru Tame no Suzumiya Haruhi no Dan) (In the school's official paperwork Kyon renamed it "Support the Student Body by Overworking to Make the World a Better Place Student Service Brigade") to investigate mysterious events. Haruhi soon recruits three additional members: the laconic bibliophile Yuki Nagato, the shy and timid Mikuru Asahina, and the unflappable transfer student Itsuki Koizumi. These members soon reveal themselves to Kyon, to be the types of extraordinary characters that Haruhi seeks. They have been sent by their respective secret organizations to observe Haruhi — who is unaware that she possesses destructive reality warping powers — and to prevent these powers from being unleashed. Each of the three believe that it would be dangerous were Haruhi to discover she had such powers. Together with Kyon, they work to keep life interesting for Haruhi and to prevent her from becoming bored enough to imagine a new world, as they and their organizations fear that this would destroy the current world.</p>
